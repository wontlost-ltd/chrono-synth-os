#!/usr/bin/env node

import { writeFileSync } from 'node:fs';
import { parseArgs } from 'node:util';
import { DESKTOP_MIGRATIONS } from '../dist/src/migrations/desktop/index.js';
import { renderRustModule } from '../dist/src/renderers/sqlite-rust-module.js';

const usage =
  'usage: schema-dsl-render-rust --out <path> [--header <text>] [--package-name <name>]';

let values;
try {
  ({ values } = parseArgs({
    options: {
      out: { type: 'string', short: 'o' },
      header: { type: 'string', short: 'H', default: 'Generated by @chrono/schema-dsl' },
      'package-name': { type: 'string', default: '@chrono/schema-dsl' },
      help: { type: 'boolean' },
    },
  }));
} catch (error) {
  console.error(error instanceof Error ? error.message : String(error));
  console.error(usage);
  process.exit(2);
}

if (values.help) {
  console.log(usage);
  process.exit(0);
}

if (!values.out) {
  console.error(usage);
  process.exit(2);
}

const rust = renderRustModule({
  migrations: DESKTOP_MIGRATIONS,
  packageHeader: `${values.header} (${values['package-name']})`,
});

writeFileSync(values.out, rust);
console.log(`wrote ${values.out} (${rust.length} bytes)`);
